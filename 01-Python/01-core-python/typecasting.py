# Type Casting Examples

# String to integer
age = "18"
age = int(age)
print(age + 2)

# String to float
price = "99.5"
price = float(price)
print(price + 0.5)

# Integer to string
score = 100
score_text = str(score)
print("Your score is " + score_text)

# Float to integer
value = 12.9
value_int = int(value)
print(value_int)

# User input handling
user_age = input("Enter your age: ")
user_age = int(user_age)
print("Next year you will be:", user_age + 1)

# Checking data types
num = 10
print(type(num))
print(type(str(num)))
